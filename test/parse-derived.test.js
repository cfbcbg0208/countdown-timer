import { test } from 'node:test';
import assert from 'node:assert/strict';
import { parseFlexible } from '../src/time.js';

const NOW = new Date(2026, 5, 21, 9, 30, 15); // 2026-06-21(일) 09:30:15

const eq = (input, y, mo, d, h = 0, mi = 0, s = 0) =>
  assert.equal(
    parseFlexible(input, NOW)?.getTime(),
    new Date(y, mo - 1, d, h, mi, s).getTime(),
    `입력 "${input}"`,
  );

// 컴팩트 + 가변 길이 시간 (HHMM / HH)
test('컴팩트 + HHMM (초 없음): 260621일1300', () => eq('260621일1300', 2026, 6, 21, 13, 0, 0));
test('컴팩트 + HH: 260621일13', () => eq('260621일13', 2026, 6, 21, 13, 0, 0));
test('컴팩트 - HHMM: 260621-1300', () => eq('260621-1300', 2026, 6, 21, 13, 0, 0));
test('컴팩트 _ HHMMSS: 260621_110333', () => eq('260621_110333', 2026, 6, 21, 11, 3, 33));
test('컴팩트 무구분 12자리: 202606211300', () => eq('202606211300', 2026, 6, 21, 13, 0, 0));
test('8자리 + HHMM: 20260621 1300', () => eq('20260621 1300', 2026, 6, 21, 13, 0, 0));

// 날짜 구분자 변형
test('슬래시: 2026/06/21', () => eq('2026/06/21', 2026, 6, 21));
test('점: 2026.06.21', () => eq('2026.06.21', 2026, 6, 21));
test('점+공백+말미점: 2026. 6. 21.', () => eq('2026. 6. 21.', 2026, 6, 21));
test('점 날짜 + 시간: 2026.06.21 13:00', () => eq('2026.06.21 13:00', 2026, 6, 21, 13, 0, 0));
test('2자리 연도 대시: 26-06-21', () => eq('26-06-21', 2026, 6, 21));
test('2자리 연도 점: 26.06.21', () => eq('26.06.21', 2026, 6, 21));

// 한국어 날짜/시간
test('한국어 날짜: 2026년 6월 21일', () => eq('2026년 6월 21일', 2026, 6, 21));
test('한국어 월일(연도 생략): 6월 21일', () => eq('6월 21일', 2026, 6, 21));
test('한국어 시분초: 11시 03분 30초', () => eq('11시 03분 30초', 2026, 6, 21, 11, 3, 30));
test('한국어 풀+오후: 2026년 6월 21일 오후 1시 30분', () =>
  eq('2026년 6월 21일 오후 1시 30분', 2026, 6, 21, 13, 30, 0));

// 오전/오후
test('오전 11:30 → 11:30', () => eq('오전 11:30', 2026, 6, 21, 11, 30, 0));
test('오후 11:30 → 23:30', () => eq('오후 11:30', 2026, 6, 21, 23, 30, 0));
test('오후 12:00 → 12:00 (정오)', () => eq('오후 12:00', 2026, 6, 21, 12, 0, 0));
test('오전 12:00 → 00:00 (자정)', () => eq('오전 12:00', 2026, 6, 21, 0, 0, 0));
test('11:30 pm → 23:30', () => eq('11:30 pm', 2026, 6, 21, 23, 30, 0));

// 시간만 숫자
test('시간만 HHMM 4자리: 1300', () => eq('1300', 2026, 6, 21, 13, 0, 0));

// 회귀: 원래 형식 일부가 여전히 동작
test('회귀 260621', () => eq('260621', 2026, 6, 21));
test('회귀 110338 (날짜무효→시간)', () => eq('110338', 2026, 6, 21, 11, 3, 38));
test('회귀 유닉스 10자리', () =>
  assert.equal(parseFlexible('1700000000', NOW).getTime(), 1700000000 * 1000));
test('회귀 무효 → null', () => {
  for (const bad of ['1', '안녕', '260631', '99:99', '2026-13-01'])
    assert.equal(parseFlexible(bad, NOW), null, `"${bad}"`);
});
